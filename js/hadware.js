function to2x(x) {
    var num = x.toString(16);
    if (num.length == 1) {
        num = "0" + num;
    }
    return num;
}
function to4x(x) {
    var num = x.toString(16);
    if (num.length == 1) {
        num = "000" + num;
    }
    if (num.length == 2) {
        num = "00" + num;
    }
    if (num.length == 3) {
        num = "0" + num;
    }
    return num;
}
