function kugedit(xmlDoc) {
    xmlDoc.find("statement").each(function() {
        var name = $(this).attr("name");
        if (name == "kuge_next") {
            $(this).after('<block type="kuge_next"></block>')
        }
        if (name == "kuge_endremote") {
            $(this).after('<block type="kuge_endremote"></block>')
        }
    });
    xmlDoc.find("block").each(function() {
        var type = $(this).attr("type");
        if (type == "kuge_for") {
            if ($(this).children("statement").length == 0) {
                $(this).children("field").after('<block type="kuge_next"></block>')
            }
        }
        if (type == "kuge_remote") {
            if ($(this).children("statement").length == 0) {
                $(this).children("field").after('<block type="kuge_endremote"></block>')
            }
        }
        if (type == "kuge_if") {
            var num = parseInt($(this).children("field").text());
            if ($(this).children("statement").length == 1) {
                $(this).children("statement").after('<block type="kuge_endif"><field name="id">' + num + "</field></block>")
            }
            if ($(this).children("statement").length == 0) {
                $(this).children("field").after('<block type="kuge_endif"><field name="id">' + num + "</field></block>")
            }
        }
        if (type == "kuge_if2") {
            var num = parseInt($(this).children("field").eq(0).text());
            var m = parseInt($(this).children("field").eq(1).text());
            if ($(this).children("statement").length == 0) {
                $(this).children("field").eq(1).after('<block type="kuge_else"><field name="id2">' + m + '</field></block><block type="kuge_endif"><field name="id">' + num + "</field></block>")
            }
            if ($(this).children("statement").length == 1) {
                $(this).children("statement").eq(0).after('<block type="kuge_else"><field name="id2">' + m + '</field></block><block type="kuge_endif"><field name="id">' + num + "</field></block>")
            }
            if ($(this).children("statement").length == 2) {
                $(this).children("statement").eq(0).after('<block type="kuge_else"><field name="id2">' + m + "</field></block>");
                $(this).children("statement").eq(1).after('<block type="kuge_endif"><field name="id">' + num + "</field></block>")
            }
        }
        if (type == "kuge_label") {
            if ($(this).children("statement").length == 0) {
                $(this).after('<block type="kuge_goto"><field name="id2">0</field></block>')
            }
            if ($(this).children("statement").length == 1) {
                $(this).children("statement").eq(0).after('<block type="kuge_goto"><field name="id2">0</field></block>')
            }
        }
        if (type.indexOf('motion_function') > -1) {
            var html = $('#' + type).html();
            $(this).after(html);
            $(this).remove();
        }
        if (type.indexOf('trigger_function') > -1) {
            var html = $('#' + type).html();
            $(this).after(html);
            $(this).remove();
        }
    });
    console.log(xmlDoc)
}
